# Second line of each test case consists of N space separated strings denoting
# the elements to be stored in the trie.
# Third line of each test case consists of a String A to be searched in the stored elements.
#
# Output:
# Print the respective output in the respective line.
#
# Constraints:
# 1<=T<=20
# 1<=N<=20
#
# Example:
# Input:
# 1
# 8
